public class InformasiRuang {
    private String no_induk, nama_bangunan, nama_pemilik, alamat_pemilik, latitude, longitude, luas_lahan;

    public String getNo_induk() {
        return no_induk;
    }

    public String getNama_bangunan() {
        return nama_bangunan;
    }

    public String getNama_pemilik() {
        return nama_pemilik;
    }

    public String getAlamat_pemilik() {
        return alamat_pemilik;
    }

    public String getLatitude() {
        return latitude;
    }

    public String getLongitude() {
        return longitude;
    }

    public String getLuas_lahan() {
        return luas_lahan;
    }
}
